import { execFile } from 'child_process'
import { promisify } from 'util'
import {
  worktreePath,
  branchName,
  createWorktree,
  removeWorktree,
  discardChanges,
} from './worktree.js'
import { createStatusBoard } from './runner.js'

const execFileAsync = promisify(execFile)

// Extract OWNER/REPO from git remote URL
async function getGitHubRepo(repoPath) {
  try {
    const { stdout } = await execFileAsync('git', [
      '-C',
      repoPath,
      'config',
      '--get',
      'remote.origin.url',
    ])
    const url = stdout.trim()
    // Handle both HTTPS and SSH URLs
    // HTTPS: https://github.com/owner/repo.git or https://github.com/owner/repo
    // SSH: git@github.com:owner/repo.git
    const match = url.match(
      /(?:https:\/\/github\.com\/|git@github\.com:)([^/]+)\/([^/.]+)(?:\.git)?$/,
    )
    if (match) {
      return `${match[1]}/${match[2]}`
    }
    throw new Error(`Could not parse GitHub repo from URL: ${url}`)
  } catch (err) {
    throw new Error(`Could not determine GitHub repository: ${err.message}`)
  }
}

// Creates or checkouts a worktree at an existing branch (or creates new one)
async function ensureWorktree(repo, id, reviewKey, variantKey) {
  const wtPath = worktreePath(repo, reviewKey)
  const branch = branchName(id, variantKey)

  try {
    // Try to checkout existing branch into worktree
    await execFileAsync('git', ['-C', repo, 'worktree', 'add', wtPath, branch])
  } catch (err) {
    if (err.code === 128 && err.stderr?.includes('already exists')) {
      // Worktree directory already exists — reuse it
      return wtPath
    }
    const alreadyUsed =
      err.code === 128 && err.stderr?.match(/is already used by worktree at '([^']+)'/)
    if (alreadyUsed) {
      throw new Error(
        `Branch '${branch}' is already checked out at '${alreadyUsed[1]}'. ` +
          `Run \`git checkout -\` in that directory to switch branches, then retry.`,
      )
    }
    // Create new worktree with new branch
    await createWorktree(repo, id, variantKey)
  }
  return wtPath
}

export async function runCopilotReview(config, resultSet, opts) {
  const { id, prompt, repo, variants, prBaseBranch } = config
  const variantKeys = Object.keys(variants).filter((k) => !resultSet.variants[k]?.error)

  if (opts.dryRun) {
    printDryRun(config, resultSet, variantKeys, prBaseBranch)
    return null
  }

  const concurrency = opts.concurrency ?? variantKeys.length
  const results = {}

  let gitHubRepo
  try {
    gitHubRepo = await getGitHubRepo(repo)
  } catch (err) {
    console.error(`Fatal: ${err.message}`)
    process.exit(1)
  }

  const worktrees = {}
  for (const key of variantKeys) {
    const wtPath = await ensureWorktree(repo, id, `copilot-review-${key}`, key)
    worktrees[key] = wtPath
  }

  const labels = variantKeys.map((k) => variants[k].label)
  const board = createStatusBoard(labels)
  const queue = [...variantKeys]

  async function createPRForVariant(key) {
    const label = variants[key].label
    const baseCommit = resultSet.baseCommit
    const model = variants[key].model
    board.set(label, 'creating PR...')

    let prUrl
    try {
      // Push branch to remote
      await execFileAsync('git', ['-C', repo, 'push', '-u', 'origin', branchName(id, key)])
    } catch (err) {
      board.set(label, `push failed: ${err.message}`)
      results[key] = { label, status: 'failed', error: `push failed: ${err.message}`, prUrl: null }
      return
    }

    try {
      // Create PR with gh CLI
      const title = `agent-benchmark: ${label}`
      const shortSha = baseCommit.substring(0, 7)
      const body = `## Benchmark Variant: ${label}

**Task prompt:** ${prompt}

**Base commit:** ${shortSha}

**Model:** ${model}

---

Generated by [agent-benchmark](https://github.com/anthropics/agent-benchmark) for code quality comparison.`

      const { stdout: prOutput } = await execFileAsync('gh', [
        'pr',
        'create',
        '--draft',
        '--title',
        title,
        '--body',
        body,
        '--head',
        branchName(id, key),
        '--base',
        prBaseBranch,
        '--repo',
        gitHubRepo,
      ])

      // Extract PR URL from output (format: "https://github.com/owner/repo/pull/123")
      const prUrlMatch = prOutput.match(/https:\/\/github\.com\/[^\s]+/)
      prUrl = prUrlMatch ? prUrlMatch[0] : null

      if (!prUrl) {
        throw new Error('Could not parse PR URL from gh output')
      }
    } catch (err) {
      if (err.code === 'ENOENT') {
        board.set(label, 'gh CLI not found')
        results[key] = {
          label,
          status: 'failed',
          error: 'gh CLI not found – install GitHub CLI first',
          prUrl: null,
        }
      } else {
        board.set(label, `PR creation failed: ${err.message}`)
        results[key] = {
          label,
          status: 'failed',
          error: `PR creation failed: ${err.message}`,
          prUrl: null,
        }
      }
      return
    }

    try {
      // Request Copilot review
      const prNumber = prUrl.split('/').pop()
      await execFileAsync('gh', [
        'api',
        `repos/${gitHubRepo}/pulls/${prNumber}/requested_reviewers`,
        '-f',
        'reviewers[]=copilot-pull-request-reviewer[bot]',
      ])
    } catch (err) {
      board.set(label, `Copilot review request failed: ${err.message}`)
      results[key] = {
        label,
        status: 'created',
        error: `Copilot review request failed: ${err.message}`,
        prUrl,
      }
      return
    }

    board.set(label, 'done')
    results[key] = { label, status: 'success', error: null, prUrl }
  }

  const running = new Set()
  await new Promise((resolve) => {
    function startNext() {
      while (running.size < concurrency && queue.length > 0) {
        const key = queue.shift()
        const p = createPRForVariant(key).finally(() => {
          running.delete(p)
          startNext()
          if (running.size === 0 && queue.length === 0) resolve()
        })
        running.add(p)
      }
    }
    startNext()
  })
  board.done()

  for (const key of variantKeys) {
    try {
      const wtPath = worktreePath(repo, `copilot-review-${key}`)
      await discardChanges(wtPath)
      await removeWorktree(repo, `copilot-review-${key}`)
    } catch {
      // ignore cleanup failures
    }
  }

  return results
}

function printDryRun(config, resultSet, variantKeys, prBaseBranch) {
  console.log('Dry run – no PRs will be created.\n')
  console.log(`Repo:       ${config.repo}`)
  console.log(`Base branch: ${prBaseBranch}`)
  console.log(`Timestamp:  ${resultSet.timestamp}`)
  console.log(`\nVariants to process (${variantKeys.length}):`)
  for (const key of variantKeys) {
    console.log(`  ${config.variants[key].label}`)
  }
  const skipped = Object.keys(config.variants).filter((k) => resultSet.variants[k]?.error)
  if (skipped.length > 0) {
    console.log(`\nSkipped (errored during run):`)
    for (const key of skipped) {
      console.log(`  ${config.variants[key].label}: ${resultSet.variants[key].error}`)
    }
  }
}
